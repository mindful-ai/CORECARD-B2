# Program to print the count of vowels in the input text

# input
text = input("Enter some text: ")

# process
# output

text = text.lower()
print('a --> ', text.count('a'))
print('e --> ', text.count('e'))
print('i --> ', text.count('i'))
print('o --> ', text.count('o'))
print('u --> ', text.count('u'))
